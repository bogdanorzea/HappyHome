package com.bogdanorzea.happyhome.data;

public class Home {
    private String id;
    private String location;
    private String name;
    public Home() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLocation() {
        return location;
    }

    public String getName() {
        return name;
    }
}
